package edu_8_21;

public class Test3_StringBuffer_StringBuilder {
	public static void main(String[] args) {

//		1.StringBuffer	//동기화 되었어, 좀 느려
		StringBuffer sbf = new StringBuffer();
		sbf	.append(true)
			.append("\n")
			.append('a')
			.append("\n")
			.append("hi");
		System.out.println(sbf);
		
		System.out.println("");
		
//		2.StringBuilder	//동기화 안되있어, 빨라
		StringBuilder sb = new StringBuilder();
		sb	.append("a")
			.append("\n")
			.append("b")
			.append("\n")
			.append("c")
			.append("\n")
			.insert(2, "ddd");
		System.out.println(sb);
	}//end of main
}//end of class
