package com.hanwha.ict.dto;

public class Parent {
	private String presentParentName;
	private int parentAge;
	private String parentContact;
	
	public String getPresentParentName() {
		return presentParentName;
	}
	public void setPresentParentName(String presentParentName) {
		this.presentParentName = presentParentName;
	}
	public int getParentAge() {
		return parentAge;
	}
	public void setParentAge(int parentAge) {
		this.parentAge = parentAge;
	}
	public String getParentContact() {
		return parentContact;
	}
	public void setParentContact(String parentContact) {
		this.parentContact = parentContact;
	}
	
	
}
